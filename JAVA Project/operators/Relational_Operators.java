package operators;

public class Relational_Operators {

	public static void main(String[] args) {
		//comparision operators--> ==,<,>,<=,>=,!=
		// This operator return boolean values
		
		short a = 10;
		byte  b = 20;
		
		System.out.println(a==b);  //false
		System.out.println(a<b);   //true
		System.out.println(a>b);   //false
		System.out.println(a<=b);  //true
		System.out.println(a>=b);  //false
		System.out.println(a!=b);  //true
		
		

	}

}
