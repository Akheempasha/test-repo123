package operators;

public class Logical_Operator {

	public static void main(String[] args) {
		boolean x = true;
		boolean y=false;
		
		System.out.println(x && y); //  &&, Logical AND: returns true when both conditions are true.
		System.out.println(x || y); //  ||, Logical OR: returns true if at least one condition is true.
		System.out.println(!x);     //  !, Logical NOT: returns true when a condition is false and vice-versa
		System.out.println(!y);
			
		}
	}


